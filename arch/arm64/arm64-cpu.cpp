#define read_register(__REG) (*reg_offsets.__REG)
#define read_register_bank(__RB, __REG) (reg_offsets.__RB[__REG])
#define write_register(__REG, __VAL) (*reg_offsets.__REG) = (__VAL)
#define write_register_bank(__RB, __REG, __VAL) reg_offsets.__RB[__REG] = (__VAL)
typedef uint64_t uint64;
typedef uint32_t uint32;
typedef uint16_t uint16;
typedef uint8_t uint8;

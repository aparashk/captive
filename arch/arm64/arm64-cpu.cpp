bool arm64_cpu::handle_irq(uint32_t isr)
{
  {
    if (isr & 2) 
    {
      if (!read_register(I)) 
      {
        take_arm_exception(1, 255, 0, 0, read_pc(), 0x80);
        return true;
      }
    }
    else if (isr & 1) 
    {
      if (!read_register(F)) 
      {
        trap();
        return true;
      }
    }
    return false;
  }
}
bool arm64_cpu::handle_mmu_fault(gva_t va, const captive::arch::mmu::AddressTranslationContext& ctx)
{
  {
    if (ctx.type == captive::arch::mmu::AddressTranslationType::FETCH) 
    {
      uint32 syndrome = (uint32)ctx.private_data;
      take_arm_exception(1, 4, syndrome, va, read_pc(), 0);
    }
    else 
    {
      uint32 syndrome = (uint32)ctx.private_data;
      take_arm_exception(1, 1, syndrome, va, read_pc(), 0);
    }
    return true;
  }
}
bool arm64_cpu::handle_single_step()
{
  {
    if (!read_register(D)) 
    {
      take_arm_exception(1, 6, 0x1000022, 0, read_pc(), 0);
      return true;
    }
    return false;
  }
}
void arm64_cpu::handle_undefined_instruction()
{
  {
    take_arm_exception(1, 2, 0, 0, read_pc(), 0);
  }
}

uint32_t arm64_cpu::get_psr_from_pstate()
{
  {
    return (uint32)read_register(N) << 31 |
    (uint32)read_register(Z) << 30 | 
    (uint32)read_register(C) << 29 | 
    (uint32)read_register(V) << 28 |
    (uint32)read_register(D) << 9 |
    (uint32)read_register(A) << 8 |
    (uint32)read_register(I) << 7 |
    (uint32)read_register(F) << 6 |
    (uint32)get_el() << 2 |
    (uint32)__builtin_get_feature(A64_SPSEL);
  }
}
uint32_t arm64_cpu::get_exception_class(uint8_t current_el,uint8_t target_el,uint8_t type)
{
  {
    uint32 ec = 0;
    switch(type) 
    {
    case 0:
      {
        // Software Breakpoint
        ec = 0x38 + 4;
        break;
      }
    case 1:
      {
        // Data Fault
        if (target_el == current_el) 
        {
          ec = 0x25;
        }
        else 
        {
          ec = 0x24;
        }
        break;
      }
    case 2:
      {
        // Undefined Fault
        ec = 0;
        break;
      }
    case 3:
      {
        // Supervisor Call
        ec = 0x11 + 4;
        break;
      }
    case 4:
      {
        // Instruction Abort
        if (target_el == current_el) 
        {
          ec = 0x21;
        }
        else 
        {
          ec = 0x20;
        }
        break;
      }
    case 5:
      {
        // FPAccessTrap
        ec = 0x07;
        break;
      }
    case 6:
      {
        // Single Step
        ec = 0x32;
        break;
      }
    default: 
      {
        trap();
        break;
      }
    }
    return ec;
  }
}
void arm64_cpu::update_spsel(uint8_t v)
{
  {
    __builtin_set_feature(A64_SPSEL, v);
    return;
  }
}
void arm64_cpu::take_arm_exception(uint8_t target_el,uint8_t type,uint32_t syndrome,uint64_t vaddr,uint64_t retaddr,uint64_t voff)
{
  {
    uint32 spsr = get_psr_from_pstate();
    uint8 current_el = get_el();
    if (target_el > current_el) 
    {
      voff += 0x400;
    }
    else if (__builtin_get_feature(A64_SPSEL) == 1) 
    {
      voff += 0x200;
    }
    // Update the execution level
    set_el(target_el);
    // Update spsel
    update_spsel(1);
    if (target_el == 1) 
    {
      write_register(SPSR_EL1, spsr);
      write_register(ELR_EL1, retaddr);
      // If it's NOT an IRQ...
      if (type != 255) 
      {
        uint32 ec = get_exception_class(current_el, target_el, type);
        write_register(ESR_EL1, ((uint32)ec << 26) | ((uint32)1 << 25) | (syndrome & 0x1ffffff));
        if (type == 1 || type == 4) 
        {
          write_register(FAR_EL1, vaddr);
        }
      }
    }
    else 
    {
      trap();
    }
    write_register(D, 1);
    write_register(A, 1);
    write_register(I, 1);
    write_register(F, 1);
    uint64 vbar = read_register(VBAR);
    //write_register(PC, vbar + voff);
    write_pc(vbar + voff);
    return;
  }
}
